package Clases;

public class SmartPhone extends SmartDevice{

    int price;
    String colour;
    String os;
    double sizescreen;

    public SmartPhone(){}

    public SmartPhone(int price, String colour, String os, double sizescreen){
        this.price = price;
        this.colour = colour;
        this.os = os;
        this.sizescreen = sizescreen;
    }

}



