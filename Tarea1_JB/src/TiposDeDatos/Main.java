package TiposDeDatos;

public class Main {
    public static void main(String[] args){

        //Tipos de datos
        int i = 1;
        float f = 0.1f;
        byte b = 127; // rango entre -128 y 127
        char c = 'k';  // dato para una letra
        long numero = 311321378;
        double d = 3.244;
        boolean k = true;
        short temperatura = -10; // dato entre  -32.768 a 32.767
        String j = "juan";


        System.out.println(i);
        System.out.println(f);
        System.out.println(b);
        System.out.println(c);
        System.out.println("Mi nombre es: "+j);
        System.out.println(numero);
        System.out.println(d);
        System.out.println(k);
        System.out.println(temperatura);
    }
}
